#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == MAX_NG_INT)
	{
		ft_putstr_fd(MAX_NG_STR, fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd(n % 10 + '0', fd);
}
